// ---------------------------------------------------------------------------
// 5. GateIssue
// ---------------------------------------------------------------------------

export interface GateIssue {
  code: GateIssueCode;
  severity: 'low' | 'medium' | 'high';
  path: string;
  message: string;
  expected?: any;
  actual?: any;
}

// ---------------------------------------------------------------------------
// 6. GateResult
// ---------------------------------------------------------------------------

export interface GateResult {
  verificationStatus: GateVerdict;
  safeToEvaluateInParity: boolean;
  issues: GateIssue[];
  sentinelRuleAtRisk: SentinelRule[];
  summary: {
    symbol: string;
    market: string;
    timeframe: string;
  };
  auditHints: {
    whySafe: string[];
    whyBlocked: string[];
  };
  timestamp: string;
}
