// Signal Payload Verification Gate — Type Definitions
// Phase 1: Canonical types for gate verdict, issue codes, and Sentinel rules.

// ---------------------------------------------------------------------------
// 1. Gate Verdict
// ---------------------------------------------------------------------------

export enum GateVerdict {
  PASS = 'PASS',
  WARN = 'WARN',
  FAIL = 'FAIL',
  INSUFFICIENT = 'INSUFFICIENT',
}

// ---------------------------------------------------------------------------
// 2. Gate Issue Codes
// ---------------------------------------------------------------------------

export enum GateIssueCode {
  MISSING_FIELD = 'MISSING_FIELD',
  EMPTY_VALUE = 'EMPTY_VALUE',
  ENUM_DRIFT = 'ENUM_DRIFT',
  SHAPE_MISMATCH = 'SHAPE_MISMATCH',
  TYPE_MISMATCH = 'TYPE_MISMATCH',
  MAPPING_GAP = 'MAPPING_GAP',
  GOLDEN_RULE_CONFLICT = 'GOLDEN_RULE_CONFLICT',
  MR_BLOCK_CONDITION = 'MR_BLOCK_CONDITION',
  RECOVERY_SUSPENDED_CONDITION = 'RECOVERY_SUSPENDED_CONDITION',
  AMBIGUOUS_INPUT = 'AMBIGUOUS_INPUT',
  INSUFFICIENT_CONTEXT = 'INSUFFICIENT_CONTEXT',
  RECLASSIFICATION_PENDING = 'RECLASSIFICATION_PENDING',
}

// ---------------------------------------------------------------------------
// 3. Sentinel Rules
// ---------------------------------------------------------------------------

export enum SentinelRule {
  GOLDEN_RULE = 'GOLDEN_RULE',
  MR_GUARD = 'MR_GUARD',
  RECOVERY_SUSPENDED = 'RECOVERY_SUSPENDED',
  AMBIGUITY_BLOCK = 'AMBIGUITY_BLOCK',
  RECLASSIFICATION_INTEGRITY = 'RECLASSIFICATION_INTEGRITY',
}

